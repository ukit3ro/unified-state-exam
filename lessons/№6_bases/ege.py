import turtle

# Настройка черепахи
t = turtle.Turtle()
t.speed(0)  # максимальная скорость
screen = t.getscreen()
t.hideturtle()

# Функция для отрисовки фигуры с заполнением
def draw_shape():
    t.begin_fill()
    # Поворачиваем черепаху направо на 180 градусов и двигаемся вперед на 2 шага
    t.right(180)
    t.forward(2)

    # Серия команд для создания углового "квадрата"
    t.right(90)
    t.forward(30)
    t.right(90)
    t.forward(2)
    t.right(30)

    # Цикл для создания шестиугольной звезды
    for _ in range(6):
        t.forward(5)
        t.right(120)
        t.forward(5)
        t.right(240)

    t.end_fill()

# Отрисовка фигуры
draw_shape()

# Получаем холст и экстенты заполненной области
canvas = screen.getcanvas()


# Перебираем каждую точку в ограничивающем прямоугольнике и проверяем, находится ли она внутри заполненной области
count = 0
for x in range(-300, 300):
    for y in range(-300, 300):
        # проверяем, попадает ли точка в заполненную область
        if canvas.find_overlapping(x, y, x, y):
            # Увеличиваем счетчик если точка внутри области
            count += 1

# Выводим результат
print("Количество точек с целочисленными координатами внутри области:", count)

# Завершаем рисование
turtle.done()
